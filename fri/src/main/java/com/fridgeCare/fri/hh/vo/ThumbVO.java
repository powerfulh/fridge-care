package com.fridgeCare.fri.hh.vo;

public class ThumbVO {
	int tno , tmno;
	String dir , tname , id;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getTno() {
		return tno;
	}
	public void setTno(int tno) {
		this.tno = tno;
	}
	public int getTmno() {
		return tmno;
	}
	public void setTmno(int tmno) {
		this.tmno = tmno;
	}
	public String getDir() {
		return dir;
	}
	public void setDir(String dir) {
		this.dir = dir;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
}
